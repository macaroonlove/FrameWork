using System;

namespace Temporary.Core
{
    [Serializable]
    public class HealingMultiplierDataEffect : DataEffect<float>
    {
        public override string GetDescription()
        {
            if (value == 0)
            {
                return $"회복량을 상승·하락 시켜주세요.";
            }
            else if (value > 0)
            {
                return $"회복량  {value * 100}% 상승";
            }
            else
            {
                return $"회복량  {value * 100}% 하락";
            }
        }
    }
}